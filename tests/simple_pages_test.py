def test_request_main_menu_links(client):
    """This makes the index page"""
    response = client.get("/")
    assert response.status_code == 200
    assert b'<a href="/about" class="nav-link text-white" >About</a>' in response.data
    assert b'<a href="/page1" class="nav-link text-white">Dashboard</a>' in response.data
    assert b'<a href="/page2" class="nav-link text-white">Orders</a>' in response.data
    assert b'<a href="/page3" class="nav-link text-white">Products</a>' in response.data
    assert b'<a href="/page4" class="nav-link text-white">Team</a>' in response.data

